class NewsLoaded extends NewsState {
  final List<Post> posts;
  final List<int> bookmarks;
  final bool hasMore;

  const NewsLoaded({
    required this.posts,
    required this.bookmarks,
    required this.hasMore,
  });

  @override
  List<Object?> get props => [posts, bookmarks, hasMore];
}

